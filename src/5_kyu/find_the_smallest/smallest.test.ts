import { smallest } from "./smallest";

describe("smallest function", () => {
  test.each([
    [261235, [126235, 2, 0]],
    [209917, [29917, 0, 1]],
    [1342223, [1234223, 3, 1]],
    [187863002809, [18786300289, 10, 0]],
    [199819884756, [119989884756, 4, 0]],
    [935855753, [358557539, 0, 8]],
    [111111111, [111111111, 0, 0]],
    [1013884, [113884, 0, 1]],
    [3037074, [303774, 4, 0]],
    [2155291, [1215529, 6, 0]],
    [1128787, [1127878, 3, 6]],
  ])("when passed %p returns %p", (n: number, result: Array<number>) => {
    expect(smallest(n)).toEqual(result);
  });
});
